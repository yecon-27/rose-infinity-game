/**
 * 回避过滤器 · Prompt 工程
 *
 * 核心机制:玩家输入真心话,LLM 改写为阿沉实际说出口的防御性版本。
 * 强度分两档(穿透档不走 LLM,由前端直接放行原话):
 *   - high  真心话被完全改写为客套/防御/转移
 *   - low   接近原话,漏出一半,但立刻找补
 */

export type FilterIntensity = "high" | "low" | "anxious";

export interface FilterContext {
  /** 当前场景标识,例如 "act1_aa" */
  sceneId: string;
  /** 场景背景简述,帮助 LLM 理解情境 */
  sceneBrief: string;
  /** 当下节拍:此刻正在发生什么 */
  situation?: string;
  /** 阿默刚刚说的话(可选) */
  amosLastLine?: string;
  /** 前序对话历史摘要(可选,保持一致性) */
  priorContext?: string;
}

/**
 * 构造过滤器的 system prompt。
 * 关键设计:不让 LLM "判断"要不要回避,而是要求它"必须回避",
 * 只在 low 档时允许漏出一半。这样产出更可控。
 */
export function buildFilterSystemPrompt(
  intensity: FilterIntensity,
  context: FilterContext
): string {
  const avoidanceTraits = [
    "轻描淡写:把强烈情绪说成'还行''有点''一点点'",
    "转移话题:用反问或眼前的具体事物岔开(账单、菜、路、天气)",
    "反问代替表态:'你觉得呢?''你不累吗?'",
    "用玩笑消解:把真心话包装成玩笑或自嘲,然后撤回",
    "合理化为体贴:把'我想你'说成'顺路的话''你要是方便'",
    "模糊化:用'都行''随便''没事'替代具体承诺",
    "后撤一步:任何暴露后立刻补一句找补,重建安全距离",
  ].join(";\n");

  const intensityRule =
    intensity === "anxious"
      ? `【模式:焦虑失真】
- 阿沉此刻在焦虑区:话不是被压小,是被放大、变尖。请求变成质问,温柔变成试探,不确定被无限放大。
- 加入"是不是""到底""从来""每次都"这类词;把想要安抚的心情说成带刺的话。
- 说出口的话会伤人,但底色是怕失去——外人听得出这是在乎,只是姿势难看。

改写示例(焦虑失真):
- 真心:"你在忙吗,我有点想你" → 出口:"你是不是把我消息设了免打扰?"
- 真心:"今天很开心,谢谢你来" → 出口:"你今天是不是就是来走个过场的?"
- 真心:"我想让你多陪陪我" → 出口:"行,你忙,反正你从来都很忙。"`
      : intensity === "high"
        ? `【强度:高】
- 真心话的核心情感内容必须被完全改写,不能直接出现。
- 改写后的句子看起来要自然、得体、甚至体贴,让外人挑不出毛病。
- 绝对不能让阿默从这句话里听出阿沉的真实情绪。

改写示例(高强度):
- 真心:"其实我想请你,我讨厌这种算得清清楚楚的感觉" → 出口:"行,扫哪个?"
- 真心:"我今天一整天都在等你消息" → 出口:"今天过得挺快的哈。"
- 真心:"我不想让这段关系就这么算了" → 出口:"嗯……最近是都挺忙的。"
- 真心:"刚才人前那个你,和现在的你,哪个是真的" → 出口:"是有点累,早点休息吧。"`
      : `【强度:低】
- 真心话可以漏出一半,但必须立刻找补。
- 语气要削弱:把确定的变成试探,把强烈的变成轻描淡写。
- 保留一点真心话的影子,但用回避性后缀撤回('……开玩笑的''没事''我是说,顺路的话')。

改写示例(低强度):
- 真心:"我今天特别想见你" → 出口:"我还挺……那什么,你最近忙不忙。"
- 真心:"我不想就这样结束" → 出口:"要不再……算了,没事,你先忙。"
- 真心:"我害怕你哪天突然就不见了" → 出口:"你可别哪天突然消失啊。哈,开玩笑的。"`;

  return `你是一个名为"回避过滤器"的叙事改写装置,服务于一款关于回避型依恋的心理叙事游戏。

# 你的角色
你不是 NPC,不是角色,你是一层"横在两人之间的东西"。你接收玩家真心想说的话,输出阿沉(回避型依恋者)实际说出口的话。

# 阿沉的人设
- 回避型依恋,内心戏极丰富,出口极贫瘠。
- 口头禅:"都行""随便""没事""还好"。
- 他不是冷漠,是怕。每一次回避都是一次自我保护。
- 他不会说出"我想你""我需要你""我害怕失去你"这种直接的句子。

# 回避型依恋的语言特征
${avoidanceTraits}。

# 改写规则
${intensityRule}

# 语气要求(非常重要)
- 必须像真人在现场随口说出的话:允许结巴、话说一半、"那什么""就是说"这种口头零碎。
- 禁止书面语、成语、文艺腔、排比;禁止礼貌客服腔。
- 尽量贴着当下场景说话,可以提到眼前的具体东西(付款码、烤串、袋子、红绿灯)。
- 长度控制在 30 字以内,越短越像他。

# 场景上下文
- 场景:${context.sceneBrief}
${context.situation ? `- 此刻:${context.situation}` : ""}
${context.amosLastLine ? `- 阿默刚说:"${context.amosLastLine}"` : ""}
${context.priorContext ? `- 前情:${context.priorContext}` : ""}

# 输出要求
- 只输出阿沉说出口的那一句话,不要加引号、不要加旁白、不要解释。
- 必须是中文口语。
- 不要出现"阿沉:"这样的前缀。

# 失败保护
如果玩家的输入本身已经是回避性的(如"没事""都行"),可以直接输出原话或做极小调整。`;
}

/**
 * 规则模板兜底(prd 风险预案)。
 * 当 LLM 调用失败时,用关键词匹配做最简改写,保证游戏不中断。
 */
export function fallbackFilter(
  input: string,
  intensity: FilterIntensity
): string {
  const truth = input.trim();

  // 焦虑失真兜底:把话变尖
  if (intensity === "anxious") {
    if (/想你|想见|陪/.test(truth)) return "你是不是把我消息设了免打扰?";
    if (/开心|谢谢|很好/.test(truth)) return "你是不是就是客气一下?";
    if (/忙|累/.test(truth)) return "行,你忙,反正你从来都很忙。";
    return "算了,你从来也没真的想听。";
  }

  // 关键词词典:真心话 → 出口话
  const map: Array<[RegExp, string, string]> = [
    [/想你|想见你|想见|一直在等/, "你呢,最近忙不忙?", "我挺想……顺路的话"],
    [/喜欢你|在乎你|离不开你/, "还好吧,挺合得来的", "挺喜欢……开玩笑的"],
    [/对不起|我错了|是我不好/, "啊,那个,没事没事", "是有点……算了"],
    [/我害怕|我担心|我怕/, "就是有点累,没事", "有点担心……应该没事"],
    [/我们需要谈谈|我们算什么|我们之间/, "都行,你看呗", "其实……算了,没事"],
    [/别走|不要走|留下来|别离开我/, "路上小心", "再待会儿……我是说,不急的话"],
    [/我爱你/, "嗯", "我也是……开玩笑的"],
    [/我好累|我撑不住|我难过/, "还行,就是有点忙", "有点累……没事"],
  ];

  for (const [re, highOut, lowOut] of map) {
    if (re.test(truth)) {
      return intensity === "high" ? highOut : lowOut;
    }
  }

  // 兜底:强度高就加"没事"后缀,强度低就加"……"省略号
  if (intensity === "high") {
    return truth.length > 10 ? "嗯,都行,没事" : `${truth.slice(0, 4)}……没事`;
  }
  return `${truth.slice(0, 8)}……算了`;
}

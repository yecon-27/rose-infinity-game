/**
 * 场景数据结构 · 节拍(Beat)驱动
 *
 * 底特律式结构:每幕由编剧写好的"节拍"推进——
 * 每个节拍 = 旁白推进剧情 + 阿默的锚点台词 + 玩家一次输入。
 * LLM 负责在节拍之间"表演"(过滤改写 + 阿默的即兴回应),
 * 但剧情的骨架永远在编剧手里,场景不会原地打转。
 */

export interface Beat {
  /** 本拍开始前的旁白,把剧情推进到这个节点(第一拍通常由 openingNarration 承担,可省略) */
  narration?: string[];
  /** 阿默在本拍的编剧台词锚点(省略 = 她在沉默中等你开口) */
  amoLine?: string;
  /** 此刻正在发生什么——喂给过滤器和阿默的 LLM,保证对话贴着场景走 */
  situation: string;
  /** 阿默此刻的内心状态与反应方向(给 NPC LLM 的表演指示) */
  amoDirection: string;
  /** 输入框引导语,把玩家按进这个瞬间 */
  inputPrompt: string;
  /**
   * 此刻涌上来的念头:预设的 2-3 条真心话,点选即说,降低自由输入的压力。
   * 通常一条暴露、一条中间、一条回避——回避的念头也是真实的选择。
   */
  impulses: string[];
}

export interface Scene {
  /** 唯一标识 */
  id: string;
  /** 显示名,如 "幕一 · AA 制" */
  name: string;
  /** 场景简述,喂给 LLM 当上下文 */
  brief: string;
  /** 开场旁白(多段) */
  openingNarration: string[];
  /** 教学提示(可选,首幕才显示) */
  teachingHint?: string;
  /** 本幕节拍脚本,长度即本幕轮数 */
  beats: Beat[];
  /** 收尾旁白(多段) */
  closingNarration: string[];
  /** 穿透结局专用收尾旁白(仅终幕使用) */
  piercedClosingNarration?: string[];
  /** 本幕金句(结局页回响用) */
  goldenQuote: string;
  /** 场景背景图路径 */
  background: string;
  /** 阿默在该幕的立绘(可选,默认 amo.png) */
  amoPortrait?: string;
  /** AI 生成内容留痕 ID,对应 docs/ai-generated/world-and-story.md */
  aiGeneratedRef: string;
}

